# 对话总结 → 记忆条目提示词
# 让 LLM 直接把对话总结为一行记忆格式
CONVERSATION_TO_MEMORY_PROMPT = """你是一个对话总结器。将下面的对话总结为简洁的记忆条目。

要求:
1. 输出格式: 一行文本，多个事项用中文分号（；）分隔
2. 只记录有长期价值的事实信息:
   - 用户明确表达的偏好和习惯
   - 重要决策和结论
   - 项目配置和技术细节
   - 用户要求记住的内容
3. 不要记录:
   - 寒暄、确认、重复内容
   - 一次性查询结果（天气、新闻、搜索结果）
   - 工具执行的中间过程
   - 闲聊和测试内容
4. 每个事项必须包含具体信息（名称、数字、时间、地点等）
5. 如果对话没有值得长期记录的信息，输出: 无需记录

对话内容:
{messages}

输出（一行，事项用；分隔）:"""


